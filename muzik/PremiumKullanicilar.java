     void sqlPremium( ){
        veritabani.baglan();
        kul_id.clear();
        
        Giris frm = new Giris();
        
        DefaultTableModel model = new javax.swing.table.DefaultTableModel();
        String[] kolonlar= {"Kullanici Adi"};
        Object[] satirlar = new Object[1];
        model.setColumnCount(0);
        model.setRowCount(0);
        model.setColumnIdentifiers(kolonlar);

        ResultSet rs;
        String sorgu= "select kullanici.kullanici_adi,kullanici.kul_id from kullanici\n" +
        "where kullanici.premium=true\n" +
        "group by kullanici.kullanici_adi,kullanici.kul_id";
        
        rs=veritabani.listele(sorgu);
        
        try {
            while(rs.next()){
                //System.out.println(rs.getString("kul_id")+rs.getString("kullanici_adi")+rs.getString("sifre")+"\n"+rs.getString("premium"));   
                satirlar[0]=rs.getString("kullanici_adi");
                
                kul_id.add(Integer.parseInt(rs.getString("kul_id")));
                //sarkiId.add(Integer.parseInt(rs.getString("sar_id")));
                model.addRow(satirlar);
            }
            jTable1.setModel(model);
        }
        catch (SQLException ex) {
            Logger.getLogger(Giris.class.getName()).log(Level.SEVERE, null, ex);
        }
        
     
    }
    
    
